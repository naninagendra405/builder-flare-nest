export const getRandomTasks = (count: number = 6): SuggestedTask[] => {
  const shuffled = [...suggestedTasks].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getTasksByCategory = (category: string): SuggestedTask[] => {
  return suggestedTasks.filter((task) => task.category === category);
};

export const getTasksByPriority = (
  priority: "low" | "medium" | "high",
): SuggestedTask[] => {
  return suggestedTasks.filter((task) => task.priority === priority);
};
